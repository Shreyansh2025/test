export function groupComparableBatches(courses: CourseListing[], gradeFilter: string) {
  const map = new Map<string, CourseListing[]>();
  for (const c of courses) {
    if (!c.compareGroupId) continue;
    if (gradeFilter !== "All Grades" && c.grade !== gradeFilter) continue;
    if (!map.has(c.compareGroupId)) map.set(c.compareGroupId, []);
    map.get(c.compareGroupId)!.push(c);
  }
  const groups: { groupId: string; label: string; grade: string; subject: string; rows: CourseListing[] }[] = [];
  for (const [groupId, rows] of map) {
    if (rows.length < 2) continue;
    const sorted = [...rows].sort((a, b) => {
      const pa = a.isFree ? 0 : a.price;
      const pb = b.isFree ? 0 : b.price;
      return pa - pb;
    });
    const label = sorted[0]?.title.split(" ").slice(0, 8).join(" ") ?? groupId;
    groups.push({
      groupId,
      label,
      grade: sorted[0].grade,
      subject: sorted[0].subject,
      rows: sorted,
    });
  }
  groups.sort((a, b) => a.grade.localeCompare(b.grade) || a.subject.localeCompare(b.subject));
  return groups;
}

export function bestValueIndex(rows: CourseListing[]): number {
  if (rows.length === 0) return -1;
  let best = 0;
  let bestScore = 0;
  for (let i = 0; i < rows.length; i++) {
    const r = rows[i];
    const effectivePrice = r.isFree ? 0 : r.price;
    const hours = Math.max(r.durationHours, 1);
    const score = (r.rating * r.syllabusMatch) / (1 + effectivePrice / 1000) * Math.log1p(hours);
    if (score > bestScore) {
      bestScore = score;
      best = i;
    }
  }
  return best;
}
